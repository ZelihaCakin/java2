package miniProjeSesliHarfler;

public class main {

	public static void main(String[] args) {
		
		char harf='a';
		
			
		
		switch (harf) {
		case 'A','a':
		case 'O','o':
		case 'U','u':
		case 'I','ı':
		System.out.println("Kalın sesli harftir.");
		break;
		
		default:
		System.out.println("İnce sesli harftir.");
		
		}

	}

}
